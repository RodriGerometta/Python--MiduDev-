# En Python, existen varios tipos de datos incorporados que se utilizan para almacenar diferentes tipos de información. Algunos de los tipos de datos más comunes son:
# 1. Números: Python tiene varios tipos de números, incluyendo enteros (int), números de punto flotante (float) y números complejos (complex).
# 2. Cadenas de texto: Las cadenas de texto (str) se utilizan para almacenar texto. Se pueden definir utilizando comillas simples (' ') o comillas dobles (" ").
# 3. Listas: Las listas (list) son colecciones ordenadas y mutables de elementos. Se definen utilizando corchetes [].
# 4. Tuplas: Las tuplas (tuple) son colecciones ordenadas e
# 5. booleans: Los booleanos (bool) representan valores de verdad y pueden ser True o False.
# 6. Diccionarios: Los diccionarios (dict) son colecciones no
# 7. None: El tipo NoneType representa la ausencia de valor o un valor nulo. Se utiliza para indicar que una variable no tiene un valor asignado.
# 8. dictos: Los diccionarios (dict) son colecciones no ordenadas de pares clave-valor. Se definen utilizando llaves {}.
# 9. rangos: Los rangos (range) se utilizan para generar secuencias de números enteros. Se definen utilizando la función range().
# 10. sets: Los conjuntos (set) son colecciones no ordenadas de elementos únicos. Se definen utilizando llaves {} o la función set().

